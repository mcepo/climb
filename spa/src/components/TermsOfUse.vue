    <h3>6.4. PREMIUM AUTHOR’S ROYALTY</h3>
    <h3>6.4.1 CALCULATION OF THE PREMIUM ROYALTY</h3>
    <p>Premium Author’s “Premium Royalty” (Rpa) is calculated as follows:</p>
    <code>Rpa = (Ppd × Tdl / Spm) × R27crags × 50%</code>
    <p>where</p>
    <ol type="1">
      <li>Ppd = Topo price per download;</li>
      <li>Tdl = Total downloads of Topo per month;</li>
      <li>Spm = monthly sales of all Topos per month; and</li>
      <li>R27crags = 27 Crags’ monthly Net Revenue.</li>
    </ol>
    <p>Topo price per download (Ppd) is determined by the formula</p>
    <code>Ppd = nr × r × Q,</code>
    <p>in which</p>
    <ol type="1">
      <li>nr = the number or routes/pitches of the crag;</li>
      <li>Q = the quality factor (“Q-Factor”); and</li>
      <li>
        r = 0,02 € for mainly boulder crags, and 0,05 € for mainly traditional
        or sport crags.
      </li>
    </ol>
    <p>
      Q-Factor represents the quality of the Topo and is the aggregate value of
      these subfactors (counted in when satisfied):
    </p>
    <ol type="1">
      <li>+0.2 for crag description;</li>
      <li>+0.2 for access info, approach and GPS trails;</li>
      <li>+0.2 for quality of the Topo images;</li>
      <li>+0.1 for route descriptions;</li>
      <li>+0.1 for route properties;</li>
      <li>+0.1 for route info: firs ascent and date; and</li>
      <li>+0.1 for additional images.</li>
    </ol>
    <p>
      “Net Revenue” is all revenue actually received by ClimbLine in respect of
      sales of a Premium Topo Content, less the following:
    </p>
    <ol type="1">
      <li>
        government taxes and duties (e.g., VAT, excise or sales tax,
        governmental withholdings and foreign tax withholdings) incurred by 27
        Crags in connection with the sale or commercial exploitation of the
        Premium Topo;
      </li>
      <li>
        any refunds paid to Premium Users and chargebacks relating to such
        sales; and
      </li>
      <li>
        any direct costs relating to Service maintenance and business
        operations.
      </li>
    </ol>
